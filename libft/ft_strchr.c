char	*ft_strchr(const char *s, int c)
{
	while (*s || ((*s == '\0') && (char)c == '\0'))
	{
		if (((char)*s) == ((char)c))
		{
			return ((char *)s);
		}
		s++;
	}
	return (0);
}
